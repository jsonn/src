#include "SYS.h"

#if defined(SYSLIBC_SCCS) && !defined(lint)
	RCSID("$NetBSD: getcontext.S,v 1.4.16.1 2007/08/28 17:36:31 matt Exp $")
#endif /* SYSLIBC_SCCS && !lint */

#ifdef WEAK_ALIAS
WEAK_ALIAS(getcontext, _getcontext)
#endif

ENTRY(_getcontext)
	mov	r2, r0			/* Must save argument pointer. */
	SYSTRAP(getcontext)
	_SYSCALL_CERROR_CHECK
#ifdef __thumb__
	add	r2, #36
	mov	r0, lr
	str	r0, [r2, #15*4]		/* Adjust PC. */
	mov	r0, #0
	str	r0, [r2, #0*4]		/* Return 0. */
#else
	str	lr, [r2, #(36 + 15*4)]	/* Adjust PC. */
#ifdef SOFTFLOAT
	/* Ahem. */
	/* Judging from the softfloat code we should at least save
	   the exception codes and rounding mode here. */
#endif
	mov	r0, #0
	str	r0, [r2, #(36 + 0*4)]	/* Return 0. */
#endif
	RET
END(_getcontext)
