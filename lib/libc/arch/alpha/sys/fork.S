#include "SYS.h"

#ifdef WEAK_ALIAS
WEAK_ALIAS(fork, _fork)
#endif

LEAF(_fork, 0)
	CALLSYS_ERROR(fork)
	cmovne	a4, zero, v0		/* a4 (rv[1]) != 0, child */
	RET
END(fork)
