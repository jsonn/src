#ifndef RANDOM_H
#define	RANDOM_H

#include <stdlib.h>
#include <inttypes.h>

#include "milieu.h"

#define	randomUint8() ((uint8_t)random())
#define	randomUint16() ((uint16_t)random())
#define	randomUint32() ((uint32_t)random())
#define	randomUint64() ((uint64_t)random() << 32 | random())

#endif
