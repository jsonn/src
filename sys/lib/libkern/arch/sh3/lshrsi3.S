#include <machine/asm.h>


NENTRY(__lshrsi3)
	tst	r5, r5
	bt	noshift

	neg	r5, r5
	shld	r5, r4
	rts
	 mov	r4, r0

noshift:
	rts
	 mov	r4, r0
