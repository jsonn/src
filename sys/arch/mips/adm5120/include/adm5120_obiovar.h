#ifndef _ADM5120_OBIOVAR_H_
#define _ADM5120_OBIOVAR_H_

#include <sys/types.h>
#include <sys/gpio.h>
#include <sys/ioctl.h>
#include <dev/gpio/gpiovar.h>

struct obio_attach_args {
	const char	*oba_name;	/* name of device */
	bus_dma_tag_t	oba_dt;
	bus_space_tag_t	oba_st;
	bus_addr_t	oba_addr;	/* address of device */
	int		oba_irq;	/* interrupt bit # */
	void		*oba_gpio;
	int		oba_gpio_offset;
	uint32_t	oba_gpio_mask;
};

void obio_dma_init(bus_dma_tag_t);
void obio_bus_mem_init(bus_space_tag_t, void *);

#endif /* _ADM5120_OBIOVAR_H_ */
