#include <machine/asm.h>

ENTRY_NOPROFILE(__ashrdi3)
	movl %d2,%sp@-		| 0 reg, 1 %pc, 2..4 parameters
	movml %sp@(8),%d0-%d2
	jra L2
L1:
	asrl #1,%d0
	roxrl #1,%d1
L2:
	dbra %d2,L1

	movl %sp@+,%d2
	rts
